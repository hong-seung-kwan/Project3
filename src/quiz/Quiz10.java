package quiz;

public class Quiz10 {

	public static void main(String[] args) {
		
		// 코드를 보고 풀이과정과 예상결과를 작성하세요
		int result1 = -5 + 3 * 10 / 2;
		// 더하기 < 곱하기,나누기
		// 3 * 10 -> 30
		// 30 / 2 -> 15
		// 15 + -5 -> 10
		// 10
		System.out.println(result1);
		
		int x = 1;
		int result2 = (++x) + 5;
		// 단항 > 이항
		// ++x = 2
		// 2 + 5
		// 7
		System.out.println(result2);
	}

}
