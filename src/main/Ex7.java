package main;

// 대입 연산자
public class Ex7 {

	public static void main(String[] args) {
		
		// 변수 x에 값10을 저장
		int x = 10;
		
		x += 10; // x = x + 10
		System.out.println(x); // 20
		
		x -= 5; // x = x - 5
		System.out.println(x); // 15
		
		x *= 10; // x= x * 10
		System.out.println(x); // 150
		
		x /= 10; // x = x / 10
		System.out.println(x); // 15
		
		x %= 10; // x= x % 10
		System.out.println(x); // 5
		

	}

}
